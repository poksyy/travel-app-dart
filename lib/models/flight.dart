class Flight {
  final String company;
  final String day;
  final String hour;
  final String departureCity;
  final String arrivalCity;
  final double price;

  Flight({
    required this.company,
    required this.day,
    required this.hour,
    required this.departureCity,
    required this.arrivalCity,
    required this.price,
  });
}
